function switchLanguage(lang) {
    localStorage.setItem('HRFA_LANG', lang);
    const html = document.documentElement;
    html.lang = lang;
    html.dir = (lang === 'ar') ? 'rtl' : 'ltr';

    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[lang] && translations[lang][key]) {
            el.textContent = translations[lang][key];
        }
    });

    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        const key = el.getAttribute('data-i18n-placeholder');
        if (translations[lang] && translations[lang][key]) {
            el.placeholder = translations[lang][key];
        }
    });

    document.body.style.fontFamily = (lang === 'ar') ? "'Cairo', sans-serif" : "'Outfit', sans-serif";

    // Add a quick flash effect for feedback
    document.body.style.opacity = '0.5';
    setTimeout(() => {
        document.body.style.opacity = '1';
    }, 100);

    console.log(`Language loaded: ${lang}`);
}

window.switchLanguage = switchLanguage;

window.updateContent = function () {
    const lang = document.documentElement.lang || localStorage.getItem('HRFA_LANG') || 'ar';
    switchLanguage(lang);
};

function initI18n(lang) {
    const targetLang = lang || localStorage.getItem('HRFA_LANG') || 'ar';
    switchLanguage(targetLang);
}

window.initI18n = initI18n;
